export const TRANSLATIONS: Record<Exclude<LanguageCode, 'en'>, TranslationTable> = {
  ar,
  fr,
}

export function isSupportedLanguage(language: string): language is LanguageCode {
  return SUPPORTED_LANGUAGES.some(item => item.code === language)
}

export function isRtlLanguage(language: LanguageCode): boolean {
  return language === 'ar'
}

export function getLanguageDirection(language: LanguageCode): 'ltr' | 'rtl' {
  return isRtlLanguage(language) ? 'rtl' : 'ltr'
}

function getLookupCandidates(source: string): string[] {
  const candidates = new Set<string>([source])
  const normalizedWhitespace = source.replace(/\s+/g, ' ').trim()
  candidates.add(normalizedWhitespace)

  const straightQuotes = normalizedWhitespace
    .replace(/[‘’]/g, '\'')
    .replace(/[“”]/g, '"')
  candidates.add(straightQuotes)
  candidates.add(straightQuotes.replace(/'/g, '’'))
  candidates.add(straightQuotes.replace(/"/g, '“'))
  candidates.add(straightQuotes.replace(/"/g, '”'))
  candidates.add(straightQuotes.replace(/—/g, 'â€”'))
  candidates.add(straightQuotes.replace(/â€”/g, '—'))

  if (
    (normalizedWhitespace.startsWith('"') && normalizedWhitespace.endsWith('"'))
    || (normalizedWhitespace.startsWith('“') && normalizedWhitespace.endsWith('”'))
  ) {
    candidates.add(normalizedWhitespace.slice(1, -1).trim())
  }

  return [...candidates].filter(Boolean)
}

export function translateText(value: string, language: LanguageCode): string {
  if (language === 'en' || !value.trim())
    return value

  const translations = TRANSLATIONS[language]
  const leadingWhitespace = value.match(/^\s*/)?.[0] ?? ''
  const trailingWhitespace = value.match(/\s*$/)?.[0] ?? ''
  const source = value.trim()
  const translated = getLookupCandidates(source)
    .map(candidate => translations[candidate])
    .find(Boolean)

  return translated ? `${leadingWhitespace}${translated}${trailingWhitespace}` : value
}
